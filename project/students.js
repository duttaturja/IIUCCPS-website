function login() {
    var username = document.getElementById("username").value;
    var password = document.getElementById("password").value;

    var loggedInStudent = students.find(function (student) {
        return student.username === username && student.password === password;
    });

    if (loggedInStudent) {
        localStorage.setItem("loggedInStudent", JSON.stringify(loggedInStudent));
        window.location.href = "studentprofile.html";
    } else {
        alert("Invalid username or password. Please try again.");
    }
}

document.addEventListener("DOMContentLoaded", function () {
    var loggedInStudent = JSON.parse(localStorage.getItem("loggedInStudent"));

    if (loggedInStudent) {
        document.getElementById("studentName").textContent = loggedInStudent.name;
        document.getElementById("studentPicture").src = loggedInStudent.picture;
        document.getElementById("studentID").textContent = loggedInStudent.studentID;
        document.getElementById("studentEmail").textContent = loggedInStudent.email;
        document.getElementById("studentSemester").textContent = loggedInStudent.semester;
        document.getElementById("studentSection").textContent = loggedInStudent.section;
        document.getElementById("studentCourses").textContent = loggedInStudent.course;
        
        
        if (loggedInStudent.codeforcesHandle) {
            var codeforcesLink = document.getElementById("codeforcesLink");
            codeforcesLink.href = "https://codeforces.com/profile/" + loggedInStudent.codeforcesHandle;
          }
          if (loggedInStudent.progHoursHandle) {
            var progHoursLink = document.getElementById("progHoursLink");
            var progHoursUrl = "https://proghours.com/" + loggedInStudent.progHoursHandle;
            progHoursLink.href = progHoursUrl;
            // progHoursLink.textContent = progHoursUrl;
          }
        
        } else {
          if (window.location.pathname !== "/studentlogin.html") {
            window.location.href = "studentlogin.html";
          }
        }
  });
